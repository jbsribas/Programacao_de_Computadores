#Mostrar os quadrados dos valores de 0 a 19 e de 1 a 20

print("\t == Quadrado dos valores de 0 a 19 ==")
for i in range(20): # range = intervalo
    # automaticamente irá começar de 0 e parar em 19
    print(i," quadrado: ", i**2)

print("\n \t ** o Quadrado de 1 a 20 **")
for i in range(20):
    print(i+1, "quadrado: ", (i+1)**2)
    #para mostrar de 1 a 20 precisou somar 1 dentro do print

