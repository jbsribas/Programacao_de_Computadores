# PROGRAMA que calcula e mostra a area de triangulo
# b*h/2
#dada as entradas base e altura pela tela/teclado
# = é atribuição de valores para as variaveis

base = float(input("Digite o valor da base: "))
altura = float(input("Digite o valor da altura: "))
