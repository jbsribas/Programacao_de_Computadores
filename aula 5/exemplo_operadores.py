# exemplo operadores relacionais

a = 10
b = 5
c = 7
d = 3
f = 3

print("a é maior que c? ",a>c)
print("a menor que b? ",a<b)
print("d é igual a f? ",d==f)
print("a é diferente de c? ",a!=c)
print("b <= f? ",b<=f)
print("a >=d? ",a>=d)

